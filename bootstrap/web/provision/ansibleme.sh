\${WH}║     EXAMPLE MUSIC LIMITED: \$(printf '%-35s' "\${HOSTNAME}")║\${NC}
\${WH}╚══════════════════════════════════════════════════════════════╝\${NC}

  \${YL}Site     :\${NC} ${SITE_CODE}: ${CITY}, ${COUNTRY}
  \${YL}Entity   :\${NC} ${ENTITY}
