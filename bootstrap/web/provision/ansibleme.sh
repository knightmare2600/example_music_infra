_classify_host() {
  local ip="$1" ptr="$2"
  local last_octet="${ip##*.}"
  local third_octet
  third_octet=$(echo "$ip" | awk -F. '{print $3}')

  # vRACK net (192.168.139.x) — known roles by last octet
  if [[ "$third_octet" == "139" ]]; then
    case "$last_octet" in
      8)     echo "srvnodes"     ; return ;;  # EXADNSCLD001 — DNS/BIND9
      9)     echo "ansiblehosts" ; return ;;  # EXAANSCLD001 — Ansible control node
      10|11) echo "dcs"          ; return ;;  # EXADCSCLD001/002 (vRACK face)
      *)     echo "srvnodes"     ; return ;;  # other CLD infra on vRACK
    esac
  fi

  # CLD site LAN (192.168.69.x)
  if [[ "$third_octet" == "69" ]]; then
    case "$last_octet" in
      9)     echo "ansiblehosts" ; return ;;  # EXAANSCLD001 CLD LAN face
      10|11) echo "dcs"          ; return ;;  # EXADCSCLD001/002 CLD LAN face
      *)     echo "srvnodes"     ; return ;;
    esac
  fi

  case "$last_octet" in
    10|11) echo "dcs"       ; return ;;
    253)   echo "firewalls" ; return ;;
    48)    echo "pbx"       ; return ;;
  esac

  echo "unknowns"
}

_scan_subnet() {
  local subnet="$1"
  local key="$2"

  if ! command -v prips &>/dev/null; then
    warn "prips not found — installing..."
    apt-get install -y -qq prips > /dev/null
  fi

  echo -e "  ${CYAN}Scanning ${subnet} ...${NC}"
  local found=0

  for ip in $(prips "$subnet"); do
    [[ "$ip" =~ \.(0|255)$ ]] && continue

    printf '    [*] %-17s ' "$ip"
    output=$(ssh -o BatchMode=yes \
                 -o ConnectTimeout=2 \
                 -o StrictHostKeyChecking=accept-new \
                 -o UserKnownHostsFile="${ANSIBLE_HOME}/.ssh/known_hosts" \
                 -i "$KEY_FILE" \
                 "ansible@${ip}" true 2>&1)
    rc=$?

    if [[ $rc -eq 0 ]]; then
      ptr=$(host "$ip" 2>/dev/null | awk '/pointer/ {print $NF}' | sed 's/\.$//')
      [[ -z "$ptr" ]] && ptr="(no PTR)"
      group=$(_classify_host "$ip" "$ptr")
      label="${ip}  # ${ptr}"
      printf "${GREEN}%-20s${NC} → [%s]\n" "UP" "$group"
      DISC_HOSTS_BY_GROUP[$group]+="${label}"$'\n'
      (( found++ )) || true
    else
      if echo "$output" | grep -qi "permission denied"; then
        printf "${YELLOW}AUTH FAIL${NC}\n"
        DISC_UNKNOWNS+="${ip}  # AUTH FAILED — key not deployed yet"$'\n'
      elif echo "$output" | grep -qi "connection refused"; then
        printf "REFUSED\n"
      elif echo "$output" | grep -qi "no route\|Network unreachable"; then
        printf "NO ROUTE\n"
      else
        printf "DOWN\n"
      fi
    fi
  done

  echo -e "  ${GREEN}Scan of ${subnet} complete — ${found} host(s) reachable via ansible key.${NC}"
}

