MEM_FREE=\$(awk '/MemAvailable/{print int(\$2/1024)}' /proc/meminfo)
MEM_USED=\$(( MEM_TOTAL - MEM_FREE ))
DISK=\$(df -h / | awk 'NR==2{print \$3" used of "\$2" ("\$5")"}')
ANSIBLE_VER=\$(ansible --version 2>/dev/null | head -1 || echo "not found")
